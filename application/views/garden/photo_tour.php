			<h2>4. the black rock room</h2>
			
			<!-- Photo: Sec04-01 -->
			<section class="intro-section">
				<figure>
					<img src="<?php echo site_url('assets/images/photo_tour/Sec04-01.jpg'); ?>" 
					     alt="black rocks"  
					     class="intro-img" />
				</figure>
				<p>The black rocks that stretch out before us represent a streambed leading into the ocean. All three elements 
					of a Japanese garden are prominent in this room: Plant, Water, and Rock. Odd numbers are paramount to any Japanese Garden, 
					so this black rock beach actually serves as our third waterfall in the garden, for, when the rain falls, water rushes over 
					the rocks, and cascades down into the pond. Turtle island sits directly in front, screening the view of the other side of 
					the pond, and creating an intimate space below the willow and amidst the stones with negative space extending up the hill 
					behind us underneath the shade of the Liquidamber.</p>				
			</section>
			
			<h3>What to look for:</h3>
			
			<!-- Photo: Sec04-02 -->
			<section class="image-and-content">
				<figure>
					<img src="<?php echo site_url('assets/images/photo_tour/Sec04-02.jpg'); ?>" 
					     alt="Snow-Viewing lantern" />
				</figure>
				<section>
					<p>At the lake's edge is the Yukimi-Doro, or Snow-Viewing lantern, which is one of the most popular Japanese 
						lanterns. Unfortunately, in Southern California, we cannot see first hand how the snow gracefully falls on the umbrella-like 
						structure, creating a pretty design as it accumulates.</p>
				</section>				
			</section>
			
			<!-- Photo: Sec04-03 -->
			<section class="image-and-content">
				<figure>
					<img src="<?php echo site_url('assets/images/photo_tour/Sec04-03.jpg'); ?>" 
					     alt="George Yamaguchi tree" />
				</figure>
				<section>
					<p>The George Yamaguchi tree, named for the extraordinarily talented man who trained and later sold the tree to 
						the Garden, is our most valued of all the artistic black pines in the Garden. Standing between the black rock beach and the moon 
						bridge and next to the meandering path, the Yamaguchi tree has value because of its form, age, and pedigree training by a decorated 
						Japanese-American artist in the field of bonsai— a Japanese cultural art form. Yamaguchi won ten consecutive blue ribbons from the 
						LA county fair, and had the George Yamaguchi North American Garden at the US arboretum in Washington D.C. dedicated to him in 1993. 
						He had been training the tree for 25 years at his West Los Angeles Bonsai Nursery at the time of the sale in the early 1980’s. 
						Please take a moment to appreciate the craftsmanship that is required to so perfectly sculpt artwork out of nature.</p>	
				</section>			
