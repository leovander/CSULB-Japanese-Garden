			<h2>5. the moon bridge</h2>
			
			<!-- Photo: Sec05-01 -->
			<section class="intro-section">
				<figure>
					<img src="<?php echo site_url('assets/images/photo_tour/Sec05-01.jpg'); ?>" 
					     alt="moon bridge"  
					     class="intro-img" />
				</figure>
				<p>The moon bridge is patterned after the more-highly arched drum bridges of Japan and crosses the stream that 
					cascades down from the waterfall. When reflected in the water, it becomes a full circle representing the completeness of the 
					garden, and unity. The eight bud-shaped finials atop the bridge posts represent the eight spiritual principles that enlighten 
					the path to Nirvana. This bridge is the site chosen by couples for their wedding ceremonies. Here, at the highest point along 
					the path, is a beautiful place to stop and appreciate the beauty of the garden while the waterfall behind us provides a peaceful 
					and natural soundtrack to our day.</p>				
			</section>
			
