				<section>
					<p>Get familiar with the specific species of plants throughout the garden. You will be introduced to 
						mondo grass, mock-orange tobira, Indian hawthorne, red-tip photinia, sago palms, ginkgos, pines, and bamboo to mention a few.</p>			
					<p>Tamamono means round thing in Japanese. It is a technique used in many gardens to prune shrubs to 
						mimic the natural motion and curvature of rolling hills. Take notice how these entry hedges, and hedges throughout the 
						garden have soft, round edges that gently run down to the ground.</p>
