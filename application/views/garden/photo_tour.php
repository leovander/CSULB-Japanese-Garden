					<p>The main teahouse room has a little alcove in the back. The scroll hanging in the alcove is changed with each of 
						the four seasons. Beneath the scroll is the flower vase called hanaire, meaning "Container in which flowers are simply placed". 
						The appropriate type of floral arrangement for tea ceremonies is called Ikebana. These arrangements also change seasonally.</p>	
				</section>			
			</section>
			
			<!-- Photo: Sec06-03 -->
