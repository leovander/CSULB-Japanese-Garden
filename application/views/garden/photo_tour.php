			<h2>9. the zig-zag bridge</h2>
			
			<!-- Photo: Sec09-01 -->
			<section class="intro-section">
				<figure>
					<img src="<?php echo site_url('assets/images/photo_tour/Sec09-01.jpg'); ?>" 
					     alt="zig-zag bridges"  
					     class="intro-img" />
				</figure>
				<p>Generally, zig-zag bridges in Japanese gardens are inspired by the bridges of the Mikiwa Province of Japan in 
					the 10th century that zig-zagged across eight channels branching from a single river. In our garden, the bridge’s objective is 
					to force the walker to be attentive and mindful of the current place and time – being here in the now. This bridge requires us 
					to slow down and to be excited, and appreciative of what we find at every turn. It curves sharply at two ninety degree angled 
					turns and leads us past the waterfall and into the last area of the Garden. The bridge’s turns force us to look at different 
					parts of the garden as we walk across it. First, we look in at the waterfall, and the maples and pines that cascade over it. 
					Then we look outward at the pond, the Garden, and what is going on all around. Lastly, we are forced to turn back towards the 
					path, and look at what is left of the journey ahead.</p>				
			</section>
			
			<h3>What to look for:</h3>
			
			<!-- Photo: Sec09-02 -->
			<section class="image-and-content">
