			<h2>6. the tea house room</h2>
			
			<!-- Photo: Sec06-01 -->
			<section class="intro-section">
				<figure>
					<img src="<?php echo site_url('assets/images/photo_tour/Sec06-01.jpg'); ?>" 
					     alt="Tea House"  
					     class="intro-img" />
				</figure>
				<p>Lovely white birch trees flank the path to the Tea Garden. Hidden behind large tamamono tobira’s, the teahouse 
					is a small, secluded space to pause and reflect on our journey through the garden. European white birch trees, and giant timber 
					bamboo provide the shade to keep the area cool. The Tea House is used to commemorate special occasions. The traditional tea ceremony 
					is a spiritual occasion shared by host and guests. Sliding shoji doors cover the front of the teahouse, however, tea ceremony guests 
					enter on the left side through the low nijiriguchi, or kneeling door, humbling themselves in the process, and forcing them to remove 
					armor and weaponry as the teahouse is a place of peace.</p>			
			</section>
			
