				<section>
					<p>The zig-zag bride, half-moon bridge, and moon viewing platform were all rebuilt from 2010-2012 from Brazilian 
						Ironwood. It is the strongest, and densest wood on the planet making it impervious to almost all pests, and able to withstand 
						the harshest weather conditions for a hundred years. Built by Facilities Management here on campus, these bridges have been 
						masterfully crafted to not show any joints, or fasteners; it was a pain staking process to keep them simple and aesthetically 
						pleasing. We hope these upgraded features will stand strong and support many generations of southern Californians as they walk 
						through, and appreciate our gardens.</p>
