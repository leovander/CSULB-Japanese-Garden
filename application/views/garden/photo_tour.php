				<p>The Earl Burns Miller Japanese Gardens begin once we pass the metal gate beneath the wooden arbor 
					flanked by two stone pillars. The entry courtyard showcases what is to come once inside the Garden’s 
					wooden gate. Tamamono hedges in various shades of green line the stone pathway. Both evergreen and 
					seasonal bonsai placed throughout the courtyard allow for an in depth look at the craftsmanship needed 
					to maintain these natural artworks, for bonsai and the larger artistic pruned trees in the Garden. 
					Historically inspired lanterns line the courtyard, and the large wooden gate is modeled after those 
					found in the gardens of Kyoto.</p>
			</section>
			
			<h3>What to look for:</h3>
			
			<!-- Photo: Sec01-02 -->
			<section class="image-and-content">
